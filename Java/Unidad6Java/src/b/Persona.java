package Unidad6Java.src.b;

public class Persona {
    
    int dni;
    int edad;
    String nombre;
    String apellidos;
    
    public Persona(int dni, int edad, String nombre, String apellidos) {
        this.dni = dni;
        this.edad = edad;
        this.nombre = nombre;
        this.apellidos = apellidos;
    }
    
}
